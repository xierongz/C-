/************************************************************************* 
- 输入一个字符，判别它是否为大写字母，如果是，将它转换成小写字母；如果不 
- 是，不转换。然后输出最后得到的字符。
**************************************************************************/

#include <stdio.h>

int main()
{
	char a;
	printf("请输入一个英文字符：\n");
	scanf("%c",&a);
	a = (a>='A' && a<='Z') ? a+=32 : a;
	printf("%c\n",a);
	return 0;	
}
  
