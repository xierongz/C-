/**************************************************************
- 求 Sn = a+aa+aaa+...+aa...a之值，其中a是一个数字，n表示a的
- 位数，例如：
- 			2+22+222+2222+22222 (此时n=5)
- n由键盘输入。
**************************************************************/

#include <stdio.h>
#include <math.h>

int main()
{
	int n, a, i, sum=0,an;
	printf("请输入位数n和数字a(1<=a<=9),逗号间隔: ");
	scanf("%d,%d",&n,&a);
	for(i=0; i<n; i++)
	{
		an += a*pow(10,i);
		sum += an;
		printf("%d\n",an);
		
	}
	printf("Sn = %d\n",sum);
	return 0;	
} 
