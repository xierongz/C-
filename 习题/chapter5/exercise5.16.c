/**********************************************
- 输出以下图案：
-    *
-   ***
-  ***** 
- *******
-  *****
-   ***
-    *
*********************************************/

#include <stdio.h>

int main()
{
	int i,j,k=3;
	for(i=1; i<=k+1; i++)
	{
		for(j=1; j<=k+1-i; j++)
			printf(" ");
		for(j = 1; j<=2*i-1; j++)
			printf( "*" );
		printf( "\n" );
	}
	for(i=k; i>0; i--)
	{
		for(j=k-i; j>=0; j--)
			printf( " " );
		for(j=2*i-2; j>=0; j--)
			printf( "*" );
		printf( "\n" );
	}		
}  
