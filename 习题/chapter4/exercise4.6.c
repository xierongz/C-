/*****************************************************************
- 有一个函数：
- 			{ x 		(x < 1)
- 		y = { 2x - 1 	(1 <= x < 10)
-			{ 3x - 11 	(x >= 10) 
- 写程序，输入x的值，输出y相应的值。
*****************************************************************/

#include <stdio.h>

int main()
{
	double x,y;
	scanf("%lf",&x);
	printf("y的值为："); 
	if(x < 1)
		y = x;
	else
		y = (x>=1 && x<10) ? 2*x - 1 : 3*x - 11;
	printf("%lf\n",y);
	return 0;
} 
